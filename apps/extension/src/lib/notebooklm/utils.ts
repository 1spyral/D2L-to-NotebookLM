export const ALLOWED_EXTENSIONS = new Set([
  "pdf",
  "txt",
  "md",
  "docx",
  "csv",
  "epub",
  "avif",
  "bmp",
  "gif",
  "ico",
  "jp2",
  "png",
  "webp",
  "tif",
  "tiff",
  "heic",
  "heif",
  "jpeg",
  "jpg",
  "jpe",
  "3g2",
  "3gp",
  "aac",
  "aif",
  "aifc",
  "aiff",
  "amr",
  "au",
  "avi",
  "cda",
  "m4a",
  "mid",
  "mp3",
  "mp4",
  "mpeg",
  "ogg",
  "opus",
  "ra",
  "ram",
  "snd",
  "wav",
  "wma",
]);

export function isSupportedFile(fileName: string): boolean {
  const extension = fileName.split(".").pop()?.toLowerCase();
  return !!extension && ALLOWED_EXTENSIONS.has(extension);
}
